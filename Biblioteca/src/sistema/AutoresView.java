    private void tableAutoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableAutoresMouseClicked

        int x = tableAutores.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tableAutores.getModel();
        
        int id;
        String nome = model.getValueAt(x, 0).toString();
        String data = model.getValueAt(x, 1).toString();
        String dataISO;
        dataISO = date.parseDataIso(data);
        
        autores.setNome(nome);
        autores.setData_nascimento(dataISO);
        
        id = banco.acharID(autores);
        autores.setId(id);
        
        textNome.setText(nome);
        textDataDeNascimento.setText(data);
        
    }//GEN-LAST:event_tableAutoresMouseClicked

    public void atualizarAutor()
    
    {
        Connection con;
        try
        {
            con = banco.Conectar();
            Statement stmt =con.createStatement();					
            String sqlStmt =  "SELECT "
                             +"       nome, "
                             +"       data_nascimento"
                             +"   FROM "
                             +"       autores "
                             +"   ORDER BY "
                             +"           nome ASC";
            ResultSet rSet = stmt.executeQuery(sqlStmt); 

            DefaultTableModel model = (DefaultTableModel) tableAutores.getModel(); 

            int x = model.getRowCount();  
            for(int a = 0; a < x; a++)  
            {  
                model.removeRow(0);  
            }  


            while (rSet.next()) 
            {
                String nome, data, dataBR;
                nome = rSet.getString("nome");
                data = rSet.getString("data_nascimento");
                dataBR = date.parseDataBra(data);

                model.addRow(new String[] {nome, dataBR} );
            }

        }
        
        catch(Exception e){

            JOptionPane.showMessageDialog(null,"Erro -> "+e.getMessage().toString()); 	

        }
    }
    
    public void limparCampos()
    {
        textNome.setText(null);
        textDataDeNascimento.setText(null);
        textNome.requestFocus();
    }
